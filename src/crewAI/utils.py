# ANSI escape codes for colors
class Colors:
    HEADER = '\033[95m'  # Magenta
    BLUE = '\033[94m'
    CYAN = '\033[96m'
    GREEN = '\033[92m'
    WARNING = '\033[93m' # Yellow
    FAIL = '\033[91m'    # Red
    ENDC = '\033[0m'     # Resets color
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'
    INFO = '\033[94m' # Blue for general info
    DEBUG = '\033[90m' # Grey for debug